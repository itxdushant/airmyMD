               <JetValidationErrors class="mb-4" />
                <div v-if="status" class="mb-4 font-medium text-sm text-green-600">
                    {{ status }}
                </div>
                <form @submit.prevent="submit">
                   <div class="inputs_div">
                      <input type="email" v-model="form.email" id="email" required autofocus placeholder="Email">
                      <input type="password" placeholder="Password"  id="password" v-model="form.password" required autocomplete="current-password">
                      <input type="password" placeholder="Password Confirmation" id="password_confirmation" v-model="form.password_confirmation" required autocomplete="current-password">
                      <button class="btn_sign" :class="{ 'opacity-25': form.processing }" :disabled="form.processing">Sign Up</button>
